#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*cpy;

	i = 0;
	cpy = s1;
	while (*s1 != 0)
		s1++;
	while (i < n && *s2 != 0)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = 0;
	return (cpy);
}
