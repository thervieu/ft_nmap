#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	x;

	x = n;
	if (n < 0)
	{
		ft_putchar('-');
		x = -x;
	}
	if (x < 10)
		ft_putchar(x + '0');
	else
	{
		ft_putnbr(x / 10);
		ft_putchar(x % 10 + '0');
	}
}
